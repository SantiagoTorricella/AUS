/* 

STL --> Standard Templates Library
Es un implementacionde muchas estructuras de datos y algoritmos

La componen:
    - Contenedores --> colecciones de objetos y tipos primitivos (array, vectores, mapas, colas)
    - Algoritmos --> para usar con los datos de las estrucutras anteriores (busqueda, maximo, minimo)
    - Iteradores --> punteros para recorrer o generar indices (avances, retrocesos)

    - Contenedores Secuenciales --> Mantienen el orden de los elementos insertados
    - Contenedores Adaptables --> Poseen una interfaz diferente para contenedores secuenciales
    - '''''''''''' Asociativos --> Implementan un ordenamiento de datos para una busqueda mas rapida
    - '''''''''''' ''''''''''' Desordenados --> Igual que el anterior pero sin que se preserve un orden para la busqueda

 */